package pro;

public class ArrayString {
	
	    public static void main(String[] args) {
	        String[] strArray = {"Hello", "World", "Java", "Programming"}; 

	        
	        for (int i = 0; i < strArray.length; i++) {
	            System.out.println("Element at index " + i + ": " + strArray[i]);
	        }
	    }
	}

