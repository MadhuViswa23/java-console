package pro;

public class RelationalOperator {

	    public static void main(String[] args) {
	     
	        int a = 10;
	        int b = 2;

	       
	        System.out.println("a = 10");
	        System.out.println("b = 2");
	        System.out.println("a > b: " + (a > b));  
	        System.out.println("a != b: " + (a != b));
	        System.out.println("a == b: " + (a == b)); 
	       
	        
	        System.out.println("a < b: " + (a < b));   
	        System.out.println("a >= b: " + (a >= b)); 
	        System.out.println("a <= b: " + (a <= b)); 
	    }
	}


